    public void acesso(int i){
        if(i == 1){
            CadastroMenu.setEnabled(true);
            SolicitaMenu.setEnabled(true);
            ConsultaMenu.setEnabled(true);
            Cad_aluguel.setEnabled(true);
            }
        else if( i == 2){
            CadastroMenu.setEnabled(true);
            SolicitaMenu.setEnabled(true);
            ConsultaMenu.setEnabled(true);
            Cad_cliente.setEnabled(false);
            consultaCliente.setEnabled(false);
        }else{
            
            
            ConsultaMenu.setEnabled(true);
        }
        
    }

