    public void readJTable() throws ClassNotFoundException {
        DefaultTableModel modelo = (DefaultTableModel) jtb_tabela.getModel();
        modelo.setNumRows(0);
        MotoristaDAO md = new MotoristaDAO();

        for (Motorista m : md.Buscar()) {
            modelo.addRow(new Object[]{
                m.getId_motorista(),
                m.getNome(),
                m.getSobrenome(),
                m.getRg(),
                m.getCpf(),
                m.getTelefone(),
                m.getCelular(),
                m.getValidade(),
                m.getRegistro(),
                m.getUf(),
                m.getCidade(),
                m.getRua(),
                m.getCep(),
                m.getNumero(),
                m.getBairro(),
                m.getSexo()
            });

        }
    }

