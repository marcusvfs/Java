    public boolean verifica() {
        if (jtf_estabelecimento.getText().equals("")) {
            return false;

        }
        if (jta_motivo.getText().equals("")) {
            return false;

        }
        if (jtf_km.getText().equals("")) {
            return false;
        }
        if (jft_data.getText().equals("  /  /    ")) {
            return false;
        }
        if (jtf_desc.getText().equals("  :  ")) {
            return false;
        }
        if (jft_valor.getText().equals("")) {
            return false;
        } else {
            return true;
        }
    }

