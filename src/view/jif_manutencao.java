    private void jbn_cadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbn_cadastroActionPerformed
        Manutencao m = new Manutencao();

        try {
            ManutencaoDAO md = new ManutencaoDAO();
            m.setDataManutencao(jft_data.getText());
            m.setDescricao(jtf_desc.getText());
            m.setEstabelecimento(jtf_estabelecimento.getText());
            m.setIdVeic(Integer.parseInt(jtf_idveic.getText()));
            m.setKm(Integer.parseInt(jtf_km.getText()));
            m.setMotivo(jta_motivo.getText());
            m.setTelefone(jft_contato.getText());
            m.setValorTotal(Float.parseFloat(jft_valor.getText()));

            md.inserir(m);
            limpar();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(jif_abastecimento.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jbn_cadastroActionPerformed

